/**
 * Rules Installer Module
 * Syncs global rules from .agents/rules/global/ to AI platform directories
 *
 * Supports two strategies:
 * - "folder": Copy individual .md files to platform rules directory (Claude Code, Cursor)
 * - "file": Merge all .md files into a single managed file (Antigravity, Windsurf, Codex)
 */

const fs = require("fs");
const path = require("path");
const platforms = require("./platforms");

// Path to rules in the cached sync-repo
const CACHE_DIR = path.join(platforms.HOME, ".agentools-cache");
const REPO_RULES_DIR = path.join(CACHE_DIR, ".agents", "rules", "global");

// Marker to identify agentools-managed content in single-file platforms
const MANAGED_HEADER = "<!-- Generated by agentools - Do not edit manually -->\n";
const SECTION_SEPARATOR = "\n\n---\n\n";

// Platforms without global rules support
const SKIP_PLATFORMS = ["copilot"];

/**
 * Get all .md files from global rules directory
 * @param {string} [overrideDir] - Optional override for rules directory (useful for testing)
 * @returns {string[]} Sorted array of absolute file paths
 */
function getGlobalRuleFiles(overrideDir = null) {
  const rulesDir = overrideDir || REPO_RULES_DIR;
  if (!fs.existsSync(rulesDir)) {
    return [];
  }
  return fs
    .readdirSync(rulesDir)
    .filter((f) => f.endsWith(".md"))
    .sort()
    .map((f) => path.join(rulesDir, f));
}

/**
 * Merge multiple .md files into a single string with managed header
 * @param {string[]} files - Array of absolute file paths
 * @returns {string} Merged content with header and separators, or empty string if no files
 */
function mergeRuleFiles(files) {
  if (files.length === 0) return "";
  const contents = files.map((f) => fs.readFileSync(f, "utf-8").trim());
  return MANAGED_HEADER + contents.join(SECTION_SEPARATOR) + "\n";
}

/**
 * Install rules for a platform that uses a single file (file-type).
 * Only overwrites if the file was previously created by agentools (has MANAGED_HEADER).
 * User-created files are preserved unless force=true.
 *
 * @param {Object} platform - Platform object with rulesPath
 * @param {string} mergedContent - Merged content to write
 * @param {boolean} force - Force overwrite even if file is not managed
 * @returns {{installed: boolean, reason?: string}}
 */
function installRulesToFile(platform, mergedContent, force) {
  const rulesPath = platform.rulesPath;
  if (!rulesPath) return { installed: false, reason: "No rulesPath" };

  // Ensure parent directory exists
  const parentDir = path.dirname(rulesPath);
  if (!fs.existsSync(parentDir)) {
    fs.mkdirSync(parentDir, { recursive: true });
  }

  // Check if file already exists and is NOT managed by agentools
  if (fs.existsSync(rulesPath) && !force) {
    const existing = fs.readFileSync(rulesPath, "utf-8");
    if (!existing.startsWith(MANAGED_HEADER)) {
      return { installed: false, reason: "File exists and not managed by agentools (use --force)" };
    }
    // If managed and content is identical, skip unnecessary write
    if (existing === mergedContent) {
      return { installed: true, reason: "Already up to date" };
    }
  }

  fs.writeFileSync(rulesPath, mergedContent, "utf-8");
  return { installed: true };
}

/**
 * Install rules for a platform that uses a folder (folder-type like Claude Code, Cursor).
 * Always overwrites files when content has changed (repo is source of truth).
 * Only skips files with identical content.
 *
 * @param {Object} platform - Platform object with rulesPath
 * @param {string[]} ruleFiles - Array of source rule file paths
 * @param {boolean} _force - Reserved for future use (currently always updates changed files)
 * @returns {{installed: boolean, count: number, skipped: number}}
 */
function installRulesToFolder(platform, ruleFiles, _force) {
  const rulesDir = platform.rulesPath;
  if (!rulesDir) return { installed: false, count: 0, skipped: 0 };

  if (!fs.existsSync(rulesDir)) {
    fs.mkdirSync(rulesDir, { recursive: true });
  }

  let installedCount = 0;
  let skippedCount = 0;

  for (const srcFile of ruleFiles) {
    const filename = path.basename(srcFile);
    const destPath = path.join(rulesDir, filename);

    if (fs.existsSync(destPath)) {
      // Compare content: only skip if identical
      const srcContent = fs.readFileSync(srcFile);
      const destContent = fs.readFileSync(destPath);
      if (srcContent.equals(destContent)) {
        skippedCount++;
        continue;
      }
    }

    fs.copyFileSync(srcFile, destPath);
    installedCount++;
  }

  return { installed: installedCount > 0, count: installedCount, skipped: skippedCount };
}

/**
 * Install global rules to all detected platforms.
 *
 * @param {Object} [options]
 * @param {boolean} [options.force=false] - Force overwrite user files on file-type platforms
 * @param {string} [options.rulesDir=null] - Override rules source directory (for testing)
 * @returns {{rulesCount: number, platformsCount: number, details: Array}}
 */
function installRules(options = {}) {
  const { force = false, rulesDir = null } = options;

  const ruleFiles = getGlobalRuleFiles(rulesDir);
  if (ruleFiles.length === 0) {
    return { rulesCount: 0, platformsCount: 0, details: [] };
  }

  const mergedContent = mergeRuleFiles(ruleFiles);
  const detectedPlatforms = platforms.detectAll().map((p) => platforms.getByName(p.name));

  const details = [];
  let totalPlatforms = 0;

  for (const platformObj of detectedPlatforms) {
    if (!platformObj || SKIP_PLATFORMS.includes(platformObj.name)) continue;
    if (!platformObj.rulesPath) continue;

    let result;
    if (platformObj.rulesType === "folder") {
      result = installRulesToFolder(platformObj, ruleFiles, force);
    } else {
      result = installRulesToFile(platformObj, mergedContent, force);
    }

    details.push({
      platform: platformObj.name,
      rulesPath: platformObj.rulesPath,
      ...result,
    });

    if (result.installed) totalPlatforms++;
  }

  return {
    rulesCount: ruleFiles.length,
    platformsCount: totalPlatforms,
    details,
  };
}

module.exports = {
  installRules,
  getGlobalRuleFiles,
  mergeRuleFiles,
  installRulesToFile,
  installRulesToFolder,
  REPO_RULES_DIR,
  MANAGED_HEADER,
  SECTION_SEPARATOR,
};
